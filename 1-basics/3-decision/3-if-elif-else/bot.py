direction = (input("Towards which direction should I paint (up, down, left, right)?"))
if direction == "up":
    print("i am painting in the upward direction")
elif direction == "left":
    print("i am painting towards the left direction")
elif direction == "right":
    print("i am painting towards the right direction")
elif direction == "down":
    print("i am painting in the downwards direction")
else:
    print("that's not a direction")